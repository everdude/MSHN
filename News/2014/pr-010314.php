<h2>Mid-State Health Network is formed as Part of State Consolidation</h2>
<p>On January 1, 2014 Mid-State Health Network (MSHN) became the new Medicaid Managed Care Organization for a portion of Michigan’s behavioral health services. MSHN and its provider network are responsible for maintaining an adequate service delivery system for persons with Serious and Persistent Mental Illness, Serious Emotional Disturbances, Developmental Disabilities, and Substance Use Disorders.</p>
<p>MSHN, resulting from the Michigan Department of Community Health (MDCH) directive to consolidate the State’s Medicaid Behavioral Health Plans from 18 to 10 by creating a “Regional Entity,” is organized under Section 1204b of the Michigan Mental Health Code. MSHN is organized to serve twenty-one (21) counties through contracts with twelve (12) Community Mental Health (CMH) Programs including:</p>
<ul>
	<li>Bay-Arenac Behavioral Health</li>
	<li>Clinton-Eaton-Ingham Community Mental Health Authority</li>
	<li>Community Mental Health for Central Michigan</li>
	<li>Gratiot County Community Mental Health Authority</li>
	<li>Huron County Community Mental Health Authority</li>
	<li>Ionia County Community Mental Health Authority</li>
	<li>LifeWays Community Mental Health Authority</li>
	<li>Montcalm County Community Mental Health Authority</li>
	<li>Newaygo County Community Mental Health Authority</li>
	<li>Saginaw County Community Mental Health Authority</li>
	<li>Shiawassee County Community Mental Health Authority</li>
	<li>Tuscola County Community Mental Health Authority</li>
</ul>
<p>Through these local Community Mental Health Service Provider (CMHSP) partnerships, MSHN strives to continually improve community well-being/wellness through the provision of premiere behavioral health care and leadership through coordination of a network of community partnerships, essential to address the multiple needs for quality of life and the reduction of per capita costs, with priority focused on the most vulnerable citizens.</p>
<p>The activity of MSHN and the relationship between the new entity and its 12 Community Mental Health Program partners is largely administrative, regulatory and financial in nature. Services to consumers and their families will continue to be provided and managed locally; including access and intake, recipient rights protections and customer services. Contracts for consumer services and supports with the 12 CMH Program partner organizations will remain unchanged as a result of the new region’s creation. These arrangements will continue to be managed locally including all quality oversight, claims adjudication and payment. Access for services and customer service inquiries should still be directed to the local CMH.
