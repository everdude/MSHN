					<h3>MSHN - Compliance Monitoring Summary</h3>
				  <p>The Balanced Budget Act of 1997 (BBA), Public Law 105-33, requires that states conduct an annual evaluation of their managed care organizations (MCOs) and prepaid inpatient health plans (PIHPs) to determine the MCOs’ and PIHPs’ compliance with regulations, contractual requirements, and the state’s quality strategy. The Michigan Department of Health and Human Services (MDHHS), Behavioral Health and Developmental Disabilities Administration, has elected to complete this requirement by contracting with an external quality review organization (EQRO). Health Services Advisory Group, Inc. (HSAG), is the EQRO for MDHHS.</p>
				  <p>HSAG conducted the 2014–2015 compliance monitoring reviews and assessed the PIHPs’ compliance with federal regulations and contract requirements in 15 areas (standards) including one new standard (Disclosure of Ownership, Control, and Criminal Convictions).</p>
				  <p>This report documents the findings from HSAG’s 2014–2015 compliance review of <em>Mid-State Health Network’s</em> performance in complying with requirements in the areas listed above.</p>
